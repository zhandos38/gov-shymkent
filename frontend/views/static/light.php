<p><strong>Функции отдела:</strong>
</p>
<ul>
    <li>Получение в максимально короткие сроки полной и      достоверной диагностической информации;</li>
    <li>Разработка и внедрение в практику экономически      обоснованных, клинически эффективных и высококачественных методов      диагностики;</li>
    <li>Оказание консультативной помощи специалистам      клинических подразделений по вопросам диагностики;</li>
    <li>Скрининг на ранее выявление рака молочной железы.</li>
</ul>
