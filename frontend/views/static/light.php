<h4>Отделение лучевой диагностики
</h4>
<p>Заведующий отделением лучевой диагностики<strong> – Мурзатаев Артур Амангельдыевич, </strong>высшее образование<strong>, </strong>врач лучевой диагностики – первой квалификационной категории, стаж работы 9 лет, заведующий отделением с 2016 года.
