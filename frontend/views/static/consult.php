<p><strong>О подразделении:</strong></p>
<p>Консультативно-диагностическая поликлиника является одним из структур подразделения городского онкологического центра. Деятельность направлена на оказание специализированной и медико-санитарной помощи, а именно консультативная и диагностическая помощь больным с опухолевой патологией в соответствии с современным уровнем развития медицинской науки.</p>
<p>Ежедневно в поликлинике специализированную консультативно-диагностическую помощь получают более 150-240 пациентов. Основная задача консультативно-диагностического отделения - проводить диагностику для установления (уточнения) онкологического процесса, определения природы новообразования - злокачественного или доброкачественного.</p>
<p><strong>Основные задачи поликлиники:</strong></p>
<p>Своевременная постановка диагноза злокачественного новообразования пациентам, направленным из лечебных учреждений города и выявление ранних стадии заболевания. Все пациенты осматриваются специалистами поликлиники: онкологом, радиологом, химиотерапевтом.</p>
<p>План лечения каждого пациента составляется комиссионно, с привлечением заведующих отделений и сотрудников кафедр мед. академии. После обследования и определения плана лечения, врачи поликлиники направляют пациентов на лечение в соответствующие структуры. Большой отдел работы - оказание в различных формах консультативной и методической помощи врачам города по вопросам оказания медицинской помощи онкологическим больным. На базе поликлиники проходит обучение онкологов, терапевтов, гинекологов и среднего мед. персонала общих лечебных сетей.</p>
<p>Все больные с впервые установленным диагнозом злокачественного новообразования, а также&nbsp; повторные больные, при рецидивах обсуждаются на заседании МДГ, в котором&nbsp; участвуют заместитель главного врача по лечебной части и все&nbsp; заведующие отделениями, после чего больные направляются по профилю в стационар, где оказывается квалифицированное специализированное лечение (оперативное, лучевое и химиотерапевтическое).</p>
