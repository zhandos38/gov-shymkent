<p><strong>Консультативті-диагностикалық емхана</strong></p>
<p>Консультативті-диагностикалық емхана бөлімінің меңгерушісі, жоғары дәрежелі дәрігер, онколог-хирург Саимкулов Тұрсынбек Амангелдыулы.</p>
<p><strong>Бөлім туралы:</strong></p>
<p>КДЕ &ndash; қалалық онкологиялық орталығының бөлімше құрылымдарының бірі болып табылады. Іс-шаралар мамандандырылған және медициналық-санитарлық көмек көрсетуге бағытталған, атап айтқанда ісік патологиясы бар науқастарға медициналық ғылымның қазіргі заманғы даму деңгейіне сәйкес консультациялық-диагностикалық көмек көрсету.</p>
<p>Күн сайын емханада 150-240-тан астам науқасқа мамандандырылған консультациялық-диагностикалық көмек көрсету мүмкіндігіміз бар. Консультативтік-диагностикалық бөлімнің басты міндеті - онкологиялық процесті анықтау (нақтылау), неоплазманың сипатын анықтау үшін диагностиканы жүргізу, яғни ісктің қатерлі немесе қатерсіз екендегін нақтылау.<strong>&nbsp;</strong><strong>Емхананың негізгі міндеттері:</strong>Қаладағы медициналық мекемелерден жіберілген науқастар үшін қатерлі ісік ауруының уақтылы диагностикасы және ерте сатылы онкологиялық процесстерді анықтау бағытындағы жұмыс жүргізу. Барлық науқастар амбулаториялық-емханалық тексеруден өтіп болғансоң және тиянақты диагноз қойылғаннан кейін, міндетті түрде онколог, радиолог, химиотерапевт дәрігерлерінің кеңесі жүргізіледі.Әр пациенттің емдеу жоспарын бөлімше меңгерушілерімен бірлесе медициналық академия қызметкерлерін тарта отырып, комиссия жасайды. Тексеруден және емдеу жоспарын анықтағаннан кейін емхана дәрігерлері пациенттерді тиісті құрылымдарға емделуге жібереді. Үлкен жұмыс бағыты - онкологиялық науқастарға медициналық көмек көрсету бойынша қала дәрігерлеріне әр түрлі кеңестік-әдістемелік көмек көрсету. Емхана базасында онкологтар, терапевттер, гинекологтар және орта буынды медициналық мамандар оқытылады.Жаңадан анықталған қатерлі ісікпен ауыратын барлық науқастар, сондай-ақ рецидивті пациенттер, яғни ісіктің қайталануы кезінде, МДТ (мультидисциплинарлық топ) отырысында талқыланады, бұл құрылған топқа бас дәрігердің орынбасары және барлық бөлім меңгерушілері қатысады, содан кейін науқастар профиль бойынша ұсынылған &nbsp;мамандандырылған емдеріне (оталық, сәулелік және химиотерапиялық) жатқызылады.&nbsp;Қалалық онкологиялық орталығының емханасы Шымкент қаласының тұрғындарына қызмет көрсетедіҚабылдау 12 профиль бойынша жүзеге асырылады:</p>
